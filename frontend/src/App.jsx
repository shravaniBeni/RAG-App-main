// ─── Node Editor ────────────────────────────────────────────────────────────────

function NodeEditor({ workflow }) {
  const [nodes, setNodes] = useState(workflow.nodes);
  const [edges, setEdges] = useState(workflow.edges);
  const [selected, setSelected] = useState(null);
  const [connecting, setConnecting] = useState(null);
  const [mousePos, setMousePos] = useState(null);
  const [pan, setPan] = useState({ x:0, y:0 });
  const [panStart, setPanStart] = useState(null);
  const canvasRef = useRef(null);
  const draggingNode = useRef(null);

  useEffect(() => { setNodes(workflow.nodes); setEdges(workflow.edges); }, [workflow]);

  const handleCanvasMouseDown = (e) => {
    if (e.target === canvasRef.current || e.target.tagName === "svg" || e.target.tagName === "SVG") {
      setSelected(null);
      setPanStart({ x:e.clientX-pan.x, y:e.clientY-pan.y });
    }
  };

  const handleMouseMove = useCallback((e) => {
    const rect = canvasRef.current?.getBoundingClientRect(); if(!rect) return;
    setMousePos({ x:e.clientX-rect.left-pan.x, y:e.clientY-rect.top-pan.y });
    if (draggingNode.current) {
      const {id,ox,oy} = draggingNode.current;
      setNodes(ns=>ns.map(n=>n.id===id?{...n,pos:{x:e.clientX-ox-rect.left,y:e.clientY-oy-rect.top}}:n));
    } else if (panStart) {
      setPan({ x:e.clientX-panStart.x, y:e.clientY-panStart.y });
    }
  }, [pan, panStart]);

  const handleMouseUp = () => { draggingNode.current=null; setPanStart(null); if(connecting) setConnecting(null); };

  const startDragNode = (node,e) => {
    e.stopPropagation(); setSelected(node.id);
    const rect = canvasRef.current.getBoundingClientRect();
    draggingNode.current = { id:node.id, ox:e.clientX-node.pos.x-rect.left, oy:e.clientY-node.pos.y-rect.top };
  };

  const deleteNode = (id) => { setNodes(ns=>ns.filter(n=>n.id!==id)); setEdges(es=>es.filter(e=>e.src!==id&&e.tgt!==id)); setSelected(null); };
  const paramChange = (id,k,v) => setNodes(ns=>ns.map(n=>n.id===id?{...n,params:{...n.params,[k]:v}}:n));
  const endConnect = (tgtId) => {
    if(connecting&&connecting!==tgtId&&!edges.find(e=>e.src===connecting&&e.tgt===tgtId))
      setEdges(es=>[...es,{id:`e${Date.now()}`,src:connecting,tgt:tgtId}]);
    setConnecting(null);
  };

  const addNode = () => {
    const types=Object.keys(NODE_DEFS), type=types[Math.floor(Math.random()*types.length)], def=NODE_DEFS[type];
    setNodes(ns=>[...ns,{ id:String(Date.now()),type,pos:{x:120+Math.random()*400,y:80+Math.random()*300},label:type,desc:"New node",color:def.color,icon:def.icon,params:{param:"value"} }]);
  };

  return (
    <div style={{ flex:1,display:"flex",flexDirection:"column",minHeight:0 }}>
      <div style={{ display:"flex",alignItems:"center",gap:8,padding:"7px 14px",borderBottom:"1px solid #12122a",background:"#07070e",flexShrink:0 }}>
        <span style={{ fontSize:11,color:"#333",fontFamily:"monospace" }}>{nodes.length} nodes · {edges.length} edges</span>
        <div style={{ flex:1 }} />
        <span style={{ fontSize:10,color:"#252538" }}>drag handle to connect • click node to edit • drag canvas to pan</span>
        <button onClick={addNode} style={{ background:"#1a1a2e",border:"1px solid #2a2a42",color:"#6366f1",borderRadius:6,padding:"4px 12px",cursor:"pointer",fontSize:11,fontFamily:"monospace" }}>+ Add Node</button>
      </div>
      <div ref={canvasRef} onMouseDown={handleCanvasMouseDown} onMouseMove={handleMouseMove} onMouseUp={handleMouseUp} onMouseLeave={handleMouseUp}
        style={{ flex:1,position:"relative",overflow:"hidden",background:"#07070e",cursor:panStart?"grabbing":"default" }}>
        <svg style={{ position:"absolute",inset:0,width:"100%",height:"100%",pointerEvents:"none" }}>
          <defs><pattern id="dots" width="30" height="30" patternUnits="userSpaceOnUse" patternTransform={`translate(${pan.x%30},${pan.y%30})`}><circle cx="15" cy="15" r="1" fill="#14142a"/></pattern></defs>
          <rect width="100%" height="100%" fill="url(#dots)"/>
        </svg>
        <style>{`@keyframes dash{to{stroke-dashoffset:-18}}`}</style>
        <div style={{ position:"absolute",left:pan.x,top:pan.y,width:0,height:0 }}>
          <div style={{ position:"relative" }}>
            <EdgeLayer nodes={nodes} edges={edges} connecting={connecting} mousePos={mousePos}/>
            {nodes.map(node=>(
              <NodeCard key={node.id} node={node} selected={selected===node.id}
                onMouseDown={e=>startDragNode(node,e)} onDelete={deleteNode}
                onParamChange={paramChange} onStartConnect={setConnecting} onEndConnect={endConnect}/>
            ))}
          </div>
        </div>
      </div>
    </div>
  );
}

