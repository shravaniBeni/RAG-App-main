// ─── Syntax Highlighter ─────────────────────────────────────────────────────────

function tokenize(code, lang) {
  if (lang === "json") {
    return code
      .replace(/&/g,"&amp;").replace(/</g,"&lt;").replace(/>/g,"&gt;")
      .replace(/("(?:[^"\\]|\\.)*")(\s*:)/g,'<span style="color:#9cdcfe">$1</span>$2')
      .replace(/:\s*("(?:[^"\\]|\\.)*")/g,': <span style="color:#ce9178">$1</span>')
      .replace(/\b(true|false|null)\b/g,'<span style="color:#569cd6">$1</span>')
      .replace(/\b(-?\d+\.?\d*)\b/g,'<span style="color:#b5cea8">$1</span>');
  }
  if (lang === "dockerfile") {
    return code
      .replace(/&/g,"&amp;").replace(/</g,"&lt;").replace(/>/g,"&gt;")
      .replace(/^(FROM|RUN|COPY|WORKDIR|EXPOSE|CMD|ENV|ARG)(\s)/gm,'<span style="color:#569cd6">$1</span>$2')
      .replace(/#.*/g,'<span style="color:#6a9955">$&</span>');
  }
  return code
    .replace(/&/g,"&amp;").replace(/</g,"&lt;").replace(/>/g,"&gt;")
    .replace(/(\/\/[^\n]*)/g,'<span style="color:#6a9955">$1</span>')
    .replace(/(`(?:[^`\\]|\\.)*`)/g,'<span style="color:#ce9178">$1</span>')
    .replace(/("(?:[^"\\]|\\.)*"|'(?:[^'\\]|\\.)*')/g,'<span style="color:#ce9178">$1</span>')
    .replace(/\b(import|export|from|const|let|var|function|async|await|return|if|else|for|of|in|new|class|try|catch|throw|default)\b/g,'<span style="color:#c586c0">$1</span>')
    .replace(/\b(true|false|null|undefined)\b/g,'<span style="color:#569cd6">$1</span>')
    .replace(/\b(\d+\.?\d*)\b/g,'<span style="color:#b5cea8">$1</span>');
}

// ─── Code Generator ─────────────────────────────────────────────────────────────

function generateServerCode(workflow) {
  const nodeList = (workflow.nodes||[]).map(n=>`//   ${n.label}: ${n.desc||""}`).join("\n");
  return `/**
 * ${workflow.name} — Generated by RAG Builder
 *
 * Workflow:
${nodeList}
 */
import express from "express";
import cors from "cors";
import multer from "multer";
import pdf from "pdf-parse";
import { OpenAI } from "openai";
import { Pinecone } from "@pinecone-database/pinecone";
import { RecursiveCharacterTextSplitter } from "langchain/text_splitter";

const app = express();
app.use(cors());
app.use(express.json());
app.use(express.static("public"));

const openai   = new OpenAI({ apiKey: process.env.OPENAI_API_KEY });
const pinecone = new Pinecone({ apiKey: process.env.PINECONE_API_KEY });
const INDEX    = "rag-index";

async function getIndex() {
  const list = await pinecone.listIndexes();
  if (!list.indexes?.find(i => i.name === INDEX)) {
    await pinecone.createIndex({
      name: INDEX, dimension: 1536, metric: "cosine",
      spec: { serverless: { cloud: "aws", region: "us-east-1" } },
    });
    await new Promise(r => setTimeout(r, 8000));
  }
  return pinecone.index(INDEX);
}

let idx;
getIndex().then(i => { idx = i; console.log("✓ Pinecone ready"); });

const upload = multer({ storage: multer.memoryStorage() });
app.post("/upload", upload.single("file"), async (req, res) => {
  const { text } = await pdf(req.file.buffer);
  const splitter = new RecursiveCharacterTextSplitter({ chunkSize: 1000, chunkOverlap: 200 });
  const chunks = await splitter.splitText(text);
  const { data } = await openai.embeddings.create({ model: "text-embedding-3-small", input: chunks });
  await idx.upsert(data.map((e, i) => ({
    id: \`c-\${Date.now()}-\${i}\`,
    values: e.embedding,
    metadata: { text: chunks[i], src: req.file.originalname },
  })));
  res.json({ ok: true, chunks: chunks.length });
});

const sessions = {};
app.post("/chat", async (req, res) => {
  const { question, sid = "default" } = req.body;
  if (!sessions[sid]) sessions[sid] = [];
  const qEmbed = await openai.embeddings.create({ model: "text-embedding-3-small", input: question });
  const hits = await idx.query({ vector: qEmbed.data[0].embedding, topK: 5, includeMetadata: true });
  const context = hits.matches.filter(m => m.score > 0.5).map(m => m.metadata.text).join("\\n\\n");
  res.setHeader("Content-Type", "text/event-stream");
  const stream = await openai.chat.completions.create({
    model: "gpt-4o", stream: true, temperature: 0.2,
    messages: [
      { role: "system", content: \`Answer from context only.\\n\\nCONTEXT:\\n\${context}\` },
      ...sessions[sid].slice(-6),
      { role: "user", content: question },
    ],
  });
  let answer = "";
  for await (const c of stream) {
    const d = c.choices[0]?.delta?.content || "";
    if (d) { answer += d; res.write(\`data: \${JSON.stringify({d})}\\n\\n\`); }
  }
  res.write("data: [DONE]\\n\\n"); res.end();
  sessions[sid].push({ role:"user", content:question });
  sessions[sid].push({ role:"assistant", content:answer });
});

app.get("/health", (_, res) => res.json({ ok: true }));
app.listen(3001, () => console.log("🚀 http://localhost:3001"));
`;
}

