// ─── Edge Layer ─────────────────────────────────────────────────────────────────

function EdgeLayer({ nodes, edges, connecting, mousePos }) {
  const nodeMap = useMemo(() => Object.fromEntries(nodes.map(n=>[n.id,n])), [nodes]);
  const makePath = (x1,y1,x2,y2) => { const cx=(x1+x2)/2; return `M${x1},${y1} C${cx},${y1} ${cx},${y2} ${x2},${y2}`; };
  return (
    <svg style={{ position:"absolute", inset:0, width:"100%", height:"100%", pointerEvents:"none", overflow:"visible" }}>
      <defs>
        <marker id="arr" markerWidth="8" markerHeight="8" refX="7" refY="3" orient="auto">
          <path d="M0,0 L0,6 L8,3 z" fill="#4b5563" />
        </marker>
        <marker id="arr-live" markerWidth="8" markerHeight="8" refX="7" refY="3" orient="auto">
          <path d="M0,0 L0,6 L8,3 z" fill="#6366f1" />
        </marker>
      </defs>
      {edges.map(e => {
        const s=nodeMap[e.src], t=nodeMap[e.tgt]; if(!s||!t) return null;
        const x1=s.pos.x+NODE_W, y1=s.pos.y+NODE_H_BASE/2, x2=t.pos.x, y2=t.pos.y+NODE_H_BASE/2;
        return <path key={e.id} d={makePath(x1,y1,x2,y2)} stroke="#2d2d4a" strokeWidth="2" fill="none" markerEnd="url(#arr)" style={{strokeDasharray:"6 3",animation:"dash 1.5s linear infinite"}} />;
      })}
      {connecting && mousePos && (() => {
        const s=nodeMap[connecting]; if(!s) return null;
        const x1=s.pos.x+NODE_W, y1=s.pos.y+NODE_H_BASE/2;
        return <path d={makePath(x1,y1,mousePos.x,mousePos.y)} stroke="#6366f1" strokeWidth="2" fill="none" strokeDasharray="5 3" markerEnd="url(#arr-live)" />;
      })()}
    </svg>
  );
}

