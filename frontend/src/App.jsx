// ─── AI Call ──────────────────────────────────────────────────────────────────

async function callAI(prompt, apiKey) {
  const sys = `You are a RAG app architect. Given a description, return ONLY a JSON object (no markdown) with:
- name: string
- nodes: array of {id,type,pos:{x,y},label,desc,color,icon,params:{key:val}}
  id: sequential "1","2",...
  type: one of fileInput|processor|embeddings|vectorStore|retriever|llm|chatUI|output|webScraper|apiInput
  pos: x from 60 to 900 step ~240, y alternates 120 and 320
  color: hex from #3b82f6 #8b5cf6 #f59e0b #10b981 #f97316 #ec4899 #6366f1 #14b8a6 #a855f7 #06b6d4
  icon: relevant emoji
  params: 2-4 config params as key:"value"
- edges: array of {id,src,tgt}`;
  const res = await fetch("https://api.anthropic.com/v1/messages", {
    method:"POST",
    headers:{ "Content-Type":"application/json", "x-api-key": apiKey, "anthropic-version":"2023-06-01", "anthropic-dangerous-direct-browser-access":"true" },
    body:JSON.stringify({ model:"claude-sonnet-4-20250514", max_tokens:1800, system:sys, messages:[{role:"user",content:`RAG app: ${prompt}`}] }),
  });
  const data = await res.json();
  const raw = data.content?.[0]?.text||"";
  return JSON.parse(raw.replace(/^```(?:json)?\n?/,"").replace(/\n?```$/,"").trim());
}

